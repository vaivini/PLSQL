  DECODE(PCNFSAID.CONDVENDA,5,0,6,0,11,0,nvl(PCMOV.VLIPI,0) )) VALORIPI,                                          
  (NVL(PCMOV.QT, 0) * (DECODE(PCNFSAID.CONDVENDA,5,                                                                    
   NVL(PCMOV.PUNITCONT, 0),0) + NVL(PCMOV.VLOUTROS, 0) + NVL(PCMOV.VLFRETE, 0))) VLDEVOLUCAOBNF,                          
  (NVL(PCMOV.QT, 0) * (DECODE(PCNFSAID.CONDVENDA,5,                                                                   
   NVL(PCMOV.PUNITCONT, 0),6,                                                                   
