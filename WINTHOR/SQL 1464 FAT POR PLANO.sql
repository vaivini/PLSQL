      DECODE(PCMOV.CODOPER,'SB',0,nvl(pcmov.ST,0))),2)) 
ELSE                                                
  (DECODE(PCNFSAID.CONDVENDA, 5, 0, DECODE(NVL(PCMOVCOMPLE.BONIFIC, 'N'), 'N', NVL(PCMOV.QT, 0), 0)) * 
