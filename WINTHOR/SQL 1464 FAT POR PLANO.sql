       (ROUND(NVL(PCMOV.QT, 0) * DECODE(PCNFSAID.CONDVENDA,5,0,6,0,11,0,12,0,
       DECODE(PCMOV.CODOPER,'SB',0,nvl(pcmov.ST,0))),2)) 
