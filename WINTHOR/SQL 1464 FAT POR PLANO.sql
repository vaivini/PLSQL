   NVL(PCMOV.VLOUTROS, 0),6,                                                     
   NVL(PCMOV.VLOUTROS, 0),11,                                                    
   NVL(PCMOV.VLOUTROS, 0),12,                                                    
   NVL(PCMOV.VLOUTROS, 0)) +                              
